    compileSdk = 35

    defaultConfig {
        applicationId = "com.fuku856.povomanager"
        minSdk = 26
        targetSdk = 35
        // CI(GitHub Actions)では環境変数で上書きする。ローカルでは既定値を使用。
        versionCode = (System.getenv("VERSION_CODE") ?: "1").toInt()
        versionName = System.getenv("VERSION_NAME") ?: "1.0"
    }

    signingConfigs {
        create("release") {
            // keystore は CI で Secret から復号して KEYSTORE_FILE に渡される。
            // env が無いローカルでは署名設定を行わない(下の release ブロックでガード)。
            System.getenv("KEYSTORE_FILE")?.let { ksPath ->
                storeFile = file(ksPath)
                storePassword = System.getenv("KEYSTORE_PASSWORD")
                keyAlias = System.getenv("KEY_ALIAS")
                keyPassword = System.getenv("KEY_PASSWORD")
            }
        }
    }

    buildTypes {
        release {
            // KEYSTORE_FILE がある時のみ署名する(ローカルの未署名 release ビルドの破綻回避)。
            if (System.getenv("KEYSTORE_FILE") != null) {
                signingConfig = signingConfigs.getByName("release")
            }
            isMinifyEnabled = true
            isShrinkResources = true
            proguardFiles(
                getDefaultProguardFile("proguard-android-optimize.txt"),
                "proguard-rules.pro"
            )
        }
    }
    compileOptions {
        sourceCompatibility = JavaVersion.VERSION_17
        targetCompatibility = JavaVersion.VERSION_17
    }
    kotlinOptions {
        jvmTarget = "17"
    }
    buildFeatures {
        compose = true
    }
